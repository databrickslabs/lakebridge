  CAST(12345.678 AS FLOAT) AS float4_val,
  CAST(12345.678 AS DOUBLE) AS float8_val,
  CAST(12345 AS INT) AS int_val,
  CAST(12345 AS INT) AS integer_val,
  FROM_JSON('{"key": "value"}', 'OBJECT') AS object_val,
