-- snowflake sql:
SELECT
    'A'::CHAR(1) AS char_val,
    'A'::CHARACTER(1) AS character_val,
    12345::DECIMAL(10, 2) AS decimal_val,
    12345::NUMBER(10, 2) AS number_val,
    12345::NUMERIC(10, 2) AS numeric_val,
    '12345'::VARCHAR(10) AS varchar_val,
    PARSE_JSON('[1,2,3]')::ARRAY AS array_val,
    12345::BIGINT AS bigint_val,
    'deadbeef'::BINARY AS binary_val,
    'true'::BOOLEAN AS boolean_val,
    123::BYTEINT AS byteint_val,
    '2024-01-01'::DATE AS date_val,
    12345.678::DOUBLE AS double_val,
    12345.678::DOUBLE PRECISION AS double_precision_val,
    12345.678::FLOAT AS float_val,
    12345.678::FLOAT4 AS float4_val,
    12345.678::FLOAT8 AS float8_val,
    12345::INT AS int_val,
    12345::INTEGER AS integer_val,
    PARSE_JSON('{"key": "value"}')::OBJECT AS object_val,
    12345.678::REAL AS real_val,
    12345::SMALLINT AS smallint_val,
    '12345'::STRING AS string_val,
    '12345'::TEXT AS text_val,
    '12:34:56'::TIME AS time_val,
    '2024-01-01 12:34:56'::TIMESTAMP AS timestamp_val,
    '2024-01-01 12:34:56 +00:00'::TIMESTAMP_LTZ AS timestamp_ltz_val,
    '2024-01-01 12:34:56'::TIMESTAMP_NTZ AS timestamp_ntz_val,
    '2024-01-01 12:34:56 +00:00'::TIMESTAMP_TZ AS timestamp_tz_val,
    123::TINYINT AS tinyint_val,
    'deadbeef'::VARBINARY AS varbinary_val,
    PARSE_JSON('12345')::VARIANT AS variant_val

