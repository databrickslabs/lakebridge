  CAST(12345 AS SMALLINT) AS smallint_val,
  CAST('12345' AS STRING) AS string_val,
  CAST('12345' AS STRING) AS text_val,
  CAST('12:34:56' AS TIMESTAMP) AS time_val,
  CAST('2024-01-01 12:34:56' AS TIMESTAMP) AS timestamp_val,
  CAST('2024-01-01 12:34:56 +00:00' AS TIMESTAMP_LTZ) AS timestamp_ltz_val,
